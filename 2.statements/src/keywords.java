public class keywords {
    public static void main(String[] args){
               // there are 53 keywords in java

        // expressions
        int score =100;                          //  -- all the letters in blue is keyword (public,class,
        if(score>99){                                  //  static,void,main,int,if)
            System.out.println("you got 100");
            score = 0;
        }

        /* In this,
                    score = 100
                    score>99                     -- these are expressions
                    "you got 100"
                    score = 0 */

        /* In this,
                    int score=100;
                    if(score>90)                           --  these are statements
                    System.out.println("you got 100");
         */
    }

}
