public class concat {
    public static void main(String[] args){
        String a="keerthi";
        a.concat("vasan");
        System.out.println(a);
        System.out.println(a.concat("vasan"));
        String b=a.concat("vasan");
        System.out.println(a);
        System.out.println(b);
    }
}

