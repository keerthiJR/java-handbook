public class valueOf {
    public static void main(String[] args) {
        int a=10;
        boolean b=true;
        char c='z';
        String name1=String.valueOf(a);
        String name2=String.valueOf(b);
        String name3=String.valueOf(c);
        System.out.println(name1+"\n"+name2+"\n"+name3);
    }
}
